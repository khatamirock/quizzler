const fs = require('fs');
function formatQuestions(inputText) {
  // Split the input text into lines
  const lines = inputText.split('\n');
  
  let formattedQuestions = [];
  let currentQuestion = null;

  for (let line of lines) {
    line = line.trim();
    
    // Check if line starts with a number followed by a dot
    if (/^\d+\./.test(line)) {
      // If there's a current question, push it to the array
      if (currentQuestion) {
        formattedQuestions.push(currentQuestion);
      }
      // Start a new question
      currentQuestion = {
        question: line,
        options: [],
        answer: ''
      };
    } else if (line.startsWith('A)') || line.startsWith('B)') || line.startsWith('C)') || line.startsWith('D)')) {
      // Add option to current question
      currentQuestion.options.push(line);
    } else if (line.startsWith('Ans:')) {
      // Add answer to current question
      currentQuestion.answer = line;
    } else if (currentQuestion && line && !line.startsWith('Note:')) {
      // If there's content and we're in a question, append to the question text
      // But ignore lines starting with "Note:"
      currentQuestion.question += ' ' + line;
    }
  }

  // Add the last question if exists
  if (currentQuestion) {
    formattedQuestions.push(currentQuestion);
  }

  // Format the questions
  return formattedQuestions.map((q, index) => {
    return `${index + 1}. ${q.question}\n${q.options.join('\n')}\n${q.answer}`;
  }).join('\n\n');
}


function removeNotes(text) {
  // Split the text into lines
  const lines = text.split('\n');
  
  // Filter out lines that start with "Note:"
  const filteredLines = lines.filter(line => !line.trim().startsWith('Note:'));
  
  // Join the lines back together
  return filteredLines.join('\n');
}

function saveToFile(text, filename) {
  fs.writeFile(filename, text, (err) => {
    if (err) {
      console.error('Error writing to file:', err);
    } else {
      console.log(`Successfully saved to ${filename}`);
    }
  });


  
}



